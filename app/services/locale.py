async def get_shop_locale(shop: str) -> dict[str, object]:
    """Get locale config for a shop based on its country_code in DB."""
    info = await get_shop_info(shop)
    country = info.get("country_code", "RO") if info else "RO"
    return _LOCALE_MAP.get(country, _LOCALE_MAP["RO"])


def get_locale_by_country(country_code: str) -> dict[str, object]:
    """Get locale config by country code (sync, no DB call)."""
    return _LOCALE_MAP.get(country_code, _LOCALE_MAP["RO"])
